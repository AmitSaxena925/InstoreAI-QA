package com.capillary.VisitorMatrix.qa.framework.util;

import java.io.File;
import java.io.FileInputStream;
import java.io.FileOutputStream;
import java.io.IOException;
import java.util.Arrays;
import java.util.List;
import java.util.zip.ZipEntry;
import java.util.zip.ZipOutputStream;

public class FileZipUnzip {

	public static void zipFiles(String filesPath, String outputFileName, String outputDirectoryPath) throws IOException {
		List<String> srcFiles = Arrays.asList(filesPath.split(","));
		for (String scrfile : srcFiles) {
			String sourceFile = scrfile;
			FileOutputStream fos = new FileOutputStream(outputDirectoryPath + outputFileName);
			ZipOutputStream zipOut = new ZipOutputStream(fos);
			File fileToZip = new File(sourceFile);
			FileInputStream fis = new FileInputStream(fileToZip);
			ZipEntry zipEntry = new ZipEntry(fileToZip.getName());
			zipOut.putNextEntry(zipEntry);
			final byte[] bytes = new byte[1024];
			int length;
			while ((length = fis.read(bytes)) >= 0) {
				zipOut.write(bytes, 0, length);
			}
			zipOut.close();
			fis.close();
			fos.close();
		}
	}
	public static void zipMultipleToSinglefile(String files, String outputDirectoryPath) throws IOException {
        List<String> srcFiles = Arrays.asList(files.split(","));
        FileOutputStream fos = new FileOutputStream(outputDirectoryPath+"multiCompressed.zip");
        ZipOutputStream zipOut = new ZipOutputStream(fos);
        for (String srcFile : srcFiles) {
            File fileToZip = new File(srcFile);
            FileInputStream fis = new FileInputStream(fileToZip);
            ZipEntry zipEntry = new ZipEntry(fileToZip.getName());
            zipOut.putNextEntry(zipEntry);
 
            byte[] bytes = new byte[1024];
            int length;
            while((length = fis.read(bytes)) >= 0) {
                zipOut.write(bytes, 0, length);
            }
            fis.close();
        }
        zipOut.close();
        fos.close();
    }
}